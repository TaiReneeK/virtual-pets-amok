
public class DataTypes {

	public static void main(String[] args) {
		
		//int represent whole numbers
		int length = 5;
		int width = 3;
		int area;
		
		area = length * width;
		System.out.println("The area is " + area + " feet.");
		
		double doubleValue = 2.16440330489723961032;
		System.out.println(doubleValue);
		
		float floatValue = 2.16440330489723961032f;
		System.out.println(floatValue);
		
		int x = 10;
		double y = 4;
		System.out.println("x/y is: " + x/y);
		
		boolean fun = true;
		System.out.println(fun);
		
		//int, double, boolean etc..are Primitives
		//String are Objects
		String name = "Tai-Renee";
		
		System.out.println("Hello " + name);
		
		
		
	}

}
